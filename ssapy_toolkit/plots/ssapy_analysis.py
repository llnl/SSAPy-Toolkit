def plot_comparison(results, site_name, lat, lon, out_dir="."):
    n      = len(results)
    colors = plt.cm.tab10(np.linspace(0, 0.9, n))

    cols = min(n, 3)
    rows = (n + cols - 1) // cols
    fig, axes = plt.subplots(rows, cols,
                             figsize=(7 * cols, 5 * rows),
                             squeeze=False)
    fig.suptitle(f"Coverage comparison — Site: {site_name}",
                 fontsize=14, fontweight="bold")

    for idx, res in enumerate(results):
        ax = axes[idx // cols][idx % cols]
        frac_masked = np.ma.masked_less_equal(res["frac_smooth"], 0.0)
        im = ax.contourf(res["lon_grid"], res["lat_grid"],
                         frac_masked * 100,
                         levels=np.linspace(1e-9, 100, 21), cmap="YlOrRd",
                         extend="neither")
        fig.colorbar(im, ax=ax, label="% visible")
        ax.plot(lon, lat, marker="*", markersize=12,
                color="cyan", markeredgecolor="black", markeredgewidth=0.8,
                zorder=5)
        ax.set_title(
            f"{res['name']}\n"
            f"Site: {res['site_cov']:.1%}  Global: {res['global_mean']:.1%}",
            fontsize=10
        )
        ax.set_xlim(-180, 180)
